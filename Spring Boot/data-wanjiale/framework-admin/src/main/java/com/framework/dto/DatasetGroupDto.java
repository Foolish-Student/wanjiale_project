package com.framework.dto;

public class DatasetGroupDto {
    private String dataSourceId;
    private String dataSourceNameEn;
    private String dataSourceNameCn;
    private String datasetId;
    private String datasetNameEn;
    private String datasetNameCn;

    public String getDataSourceId() {
        return dataSourceId;
    }

    public void setDataSourceId(String dataSourceId) {
        this.dataSourceId = dataSourceId;
    }

    public String getDataSourceNameEn() {
        return dataSourceNameEn;
    }

    public void setDataSourceNameEn(String dataSourceNameEn) {
        this.dataSourceNameEn = dataSourceNameEn;
    }

    public String getDataSourceNameCn() {
        return dataSourceNameCn;
    }

    public void setDataSourceNameCn(String dataSourceNameCn) {
        this.dataSourceNameCn = dataSourceNameCn;
    }

    public String getDatasetId() {
        return datasetId;
    }

    public void setDatasetId(String datasetId) {
        this.datasetId = datasetId;
    }

    public String getDatasetNameEn() {
        return datasetNameEn;
    }

    public void setDatasetNameEn(String datasetNameEn) {
        this.datasetNameEn = datasetNameEn;
    }

    public String getDatasetNameCn() {
        return datasetNameCn;
    }

    public void setDatasetNameCn(String datasetNameCn) {
        this.datasetNameCn = datasetNameCn;
    }
}
